    void playTheme() {
        try {//tries to set and play the music for the game
            //defines the file for the music
            AudioInputStream ais = AudioSystem.getAudioInputStream( getClass().getResourceAsStream("../resources/MainTheme.wav"));
            //creates a clip
            Clip mainTheme = AudioSystem.getClip();
            //enables the clip to be played
            mainTheme.open(ais);
            
            FloatControl volumeControl = (FloatControl) mainTheme.getControl(FloatControl.Type.MASTER_GAIN);
            float volume = -30.0f; // 0.0f (full volume) and -80.0f (mute)
            volumeControl.setValue(volume);
            
            //loops the music
            mainTheme.loop(Clip.LOOP_CONTINUOUSLY);
        }catch(Exception ex){
            System.out.println(ex);
        }
               
    }

