    private void btDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDoneActionPerformed
        //Delete current cfg before for creating a new one (naming issues)
        //Creates a new cfg with the selected keybinds
        config = new ConfigGame(keyLeft,keyRight,keyDown,keyRotate,keyFall);
        //saving the config
        try {            
            config.saveConfig(txtNameCfg.getText());
        } catch (Exception ex) {
            Logger.getLogger(ControlsMenu.class.getName()).log(Level.SEVERE, "COULDNÂ´T SAVE CONFIG", ex);
        }
        //close the menu
        this.dispose();
    }//GEN-LAST:event_btDoneActionPerformed

